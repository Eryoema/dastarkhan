// Active language
let currentLang = "kz";

/**
 * Apply all translations to the DOM
 */
function applyTranslations(lang) {
  currentLang = lang;
  const t = translations[lang];
  const htmlLang = lang === "kz" ? "kk" : lang === "ru" ? "ru" : "en";
  document.documentElement.lang = htmlLang;

  // data-i18n text content
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // data-i18n-placeholder
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    const key = el.getAttribute("data-i18n-title");
    if (t[key] !== undefined) el.title = t[key];
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    const key = el.getAttribute("data-i18n-aria");
    if (t[key] !== undefined) el.setAttribute("aria-label", t[key]);
  });

  // Update lang buttons
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });

  const currentCode = { kz: "ҚЗ", ru: "РУ", en: "EN" }[lang] || lang.toUpperCase();
  document.querySelectorAll("[data-lang-current]").forEach((el) => {
    el.textContent = currentCode;
  });

  // Re-render menu cards with translated "add to cart" button
  if (typeof renderMenu === "function") renderMenu();
  if (typeof renderFeatured === "function") renderFeatured();
  if (typeof updateSearchResults === "function") updateSearchResults();
  if (typeof window.renderReservationMenu === "function") window.renderReservationMenu();
  if (typeof window.refreshThemeButton === "function") window.refreshThemeButton();
  if (typeof window.refreshAuthUI === "function") window.refreshAuthUI();
  if (typeof window.renderCartModal === "function") window.renderCartModal();
  if (typeof window.loadReviewsData === "function") window.loadReviewsData();
}

// Expose globally
window.applyTranslations = applyTranslations;
window.translations = translations;
window.getCurrentLang = () => currentLang;
